def add(a, b):
    """
    Return the sum of two numbers.

    :param a: First number.
    :param b: Second number.
    :return: Sum of a and b.
    """
    return a + b


def subtract(a, b):
    """
    Return the difference between two numbers.

    :param a: First number.
    :param b: Second number.
    :return: Difference of a and b.
    """
    return a - b

